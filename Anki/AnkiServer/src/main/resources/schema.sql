CREATE TABLE IF NOT EXISTS USERS
(
    ID BIGINT NOT NULL primary key ,
    USERNAME VARCHAR(512) NOT NULL
);

CREATE TABLE IF NOT EXISTS ENG_WORDS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL primary key,
    ENG_WORD VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS TRANSLATES
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    RU_WORD VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS USER_WORDS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL primary key,
    USER_ID BIGINT NOT NULL,
    WORD_ID BIGINT NOT NULL,
    REPEAT_COUNT BIGINT NOT NULL,
    IT_STUDY BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS GLOBAL_VALUES
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL primary key,
    VAR_NAME VARCHAR(50) NOT NULL,
    VAR_VALUE VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS RU_WORD_TRANSLATE
(
    RU_WORD_ID BIGINT NOT NULL,
    ENG_WORD_ID BIGINT NOT NULL
);